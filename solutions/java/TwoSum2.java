import java.util.Scanner; 

public class TwoSum2 {
    public static void main(String[] args){
        Scanner scnr = new Scanner(System.in); 
        int a = scnr.nextInt(); 
        int b = scnr.nextInt(); 
        int sum = a + b; 
        System.out.println(sum); 

        scnr.close(); 
    }
}
